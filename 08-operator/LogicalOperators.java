public class LogicalOperators {
    public static void main(String[] args) {
        boolean x = true;
        boolean y = false;

        System.out.println("x && y: " + (x && y));  // Logical AND: false
        System.out.println("x || y: " + (x || y));  // Logical OR: true
        System.out.println("!x: " + (!x));           // Logical NOT: false
    }
}
